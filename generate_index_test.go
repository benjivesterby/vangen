package main

import (
	"bytes"
	"testing"

	"github.com/sergi/go-diff/diffmatchpatch"
)

func TestGenerateIndex(t *testing.T) {
	testCases := []struct {
		description string
		domain      string
		r           []repository
		expectedOut string
		expectedErr error
	}{
		{
			description: "basic",
			domain:      "example.com",
			r: []repository{
				{
					Prefix: "pkg1",
					Subs:   []string{"subpkg1", "subpkg2"},
					Main:   true,
				},
				{
					Prefix: "pkg2",
					Subs:   []string{"subpkg1", "subpkg2/subsubpkg1"},
				},
			},
			expectedOut: `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<style>
* { font-family: sans-serif; }
body { margin-top: 0; }
.content { display: inline-block; }
</style>
</head>
<body>
<div class="content">

<h2>Go packages with vanity import path at example.com</h2>

<hr/>

<ul>
<li>
<a href="/pkg1">example.com/pkg1</a>
<ul><li><a href="/pkg1/subpkg1">example.com/pkg1/subpkg1</a></li><li><a href="/pkg1/subpkg2">example.com/pkg1/subpkg2</a></li></ul></li>
</ul>

<hr/>

<ul>
<li>
<a href="/pkg2">example.com/pkg2</a>
<ul><li><a href="/pkg2/subpkg1">example.com/pkg2/subpkg1</a></li><li><a href="/pkg2/subpkg2/subsubpkg1">example.com/pkg2/subpkg2/subsubpkg1</a></li></ul></li>
</ul>

<hr/>

Generated by <a href="https://4d63.com/vangen">vangen</a>.

</div>
</body>
</html>`,
			expectedErr: nil,
		},
	}

	for _, tc := range testCases {
		var out bytes.Buffer
		err := generate_index(&out, tc.domain, tc.r)
		if err != tc.expectedErr {
			t.Errorf("Test case %#v got err %#v, want %#v", tc, err, tc.expectedErr)
		} else if out.String() != tc.expectedOut {
			dmp := diffmatchpatch.New()
			diffs := dmp.DiffMain(tc.expectedOut, out.String(), false)
			t.Errorf("Test case %q got: \n%s\nAs diff:\n%s", tc.description, out.String(), dmp.DiffPrettyText(diffs))
		}
	}
}
